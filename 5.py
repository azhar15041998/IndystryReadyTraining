#5. Write a program to find the area and perimeter of a square.


length = float(input('Enter the length of the square: ')) #  length of the square.
perimeter = 4 * length  #Calculates the perimeter of the square 
area = length * length  #Calculates the Area of the square 
print('Perimeter of the square is',perimeter,'unit.')
print('Area of the square is',area,'square unit.')