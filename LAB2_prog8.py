#8. Write a program to find the volume of a cylinder.
radius = float(input('Enter radius of the Cylinder : ')) #User inputs the radius.
height = float(input('Enter the height of the cylender')) #inputs height of the cylinder 
pi = 3.14                                           #The pi value is 3.14
vol = pi * radius * radius * height   #volume of cylinder formula
print(f'The volume of the Cylinder is : {vol}') #print the volume of the cylinder
